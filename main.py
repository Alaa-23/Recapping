from __future__ import division
print ('\n\n\n   ====  Welcome to The Recapping Calculator ==== \n')

first_var = input("# Enter the first var please: ")
second_var = input("# Enter the second var please: ")

print("\n"+"The two vars added: " + str(first_var+second_var)+"\n")
print("The two vars multiplied: " + str(first_var*second_var)+"\n")
print("The two vars divided: " + str(float(first_var/second_var))+"\n")
print("The two vars exponented: " + str(first_var**second_var)+"\n")
print("The remainder of first var divided by the second: " + str(first_var%second_var)+"\n")
print("The floor division for first and second vars: " + str(first_var//second_var)+"\n")
